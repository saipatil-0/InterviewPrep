package binarytree;

/**
 *
 * @author saipati
 */
public class Node<T> {
    
    Node<T> left;
    Node<T> right;
    T value;
    
    Node(T val)
    {
        this.value = val;
    }

    public Node<T> getLeft() {
        return left;
    }

    public void setLeft(Node<T> left) {
        this.left = left;
    }

    public Node<T> getRight() {
        return right;
    }

    public void setRight(Node<T> right) {
        this.right = right;
    }

    public T getValue() {
        return value;
    }

    public void setValue(T value) {
        this.value = value;
    }
    
    
    
}
